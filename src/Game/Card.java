package Game;

public class Card {

    /**
     * Card suit
     */
    String suit;

    /**
     * Card rank
     */
    String rank;

    /**
     * Card constructor
     * @param suit Card suit
     * @param rank Card rank
     */
    Card(String suit, String rank) {
        this.suit = suit;
        this.rank = rank;
    }

}
